require("two-x")

-- Set the colorscheme
--vim.cmd('colorscheme vim')

-- Enable line numbers
vim.opt.number = true

-- Enable relative line numbers (optional)
vim.opt.relativenumber = true

-- Set smart indent
vim.opt.smartindent = true

-- Turn off line wrap
vim.opt.wrap = false

-- Set size of tabs
vim.opt.tabstop = 2

-- Set size of tabs in commands
vim.opt.shiftwidth = 2

-- Keep tabs as tabs instead of spaces
vim.opt.expandtab = false

-- Set cursor to blink
vim.opt.guicursor = "n-v-c:block-blinkon1,i-ci-ve:ver25-blinkon1,r-cr:hor20-blinkon1"

-- Don't keep highlight on search
vim.opt.hlsearch = false

-- incrementally highlight as you search
vim.opt.incsearch = true

vim.opt.scrolloff = 8

